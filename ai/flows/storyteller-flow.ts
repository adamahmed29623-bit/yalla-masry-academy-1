/**
 * هذا الملف هو محرك سرد القصص في الأكاديمية.
 * تم تصميمه ليعمل بذكاء وسلاسة داخل نظام Next.js.
 */

export async function storytellerFlow(prompt: string) {
  // هنا يتم استقبال طلب المستخدم وتحويله لقصة باللهجة المصرية الفريدة للأكاديمية
  // حالياً سنقوم بإرجاع استجابة ناجحة للتأكد من ربط الملف بـ Vercel
  
  if (!prompt) {
    throw new Error("يجب إدخال نص لبدء القصة");
  }

  const response = `أهلاً بك في الأكاديمية! قصتك عن "${prompt}" يتم تحضيرها الآن بلمسة ملكية مصرية...`;
  
  return response;
}
